void	comptehastag(char *line)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	while (line[j] != '\0')
	{
		if (line[j] == '#')
			i++;
		j++;
	}
	if (i != 4)
		ft_error("Bad tetromino");
}
